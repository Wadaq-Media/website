function changeLanguage(lang) {
    // 1. تغيير اتجاه واتجاهات الصفحة بناءً على اللغة
    const htmlTag = document.documentElement;
    htmlTag.lang = lang;
    
    if (lang === 'ar') {
        htmlTag.dir = 'rtl';
    } else {
        htmlTag.dir = 'ltr';
    }

    // 2. تحديث العنوان الرئيسي للموقع في التبويب
    const siteTitle = document.getElementById('site-title');
    if (siteTitle) {
        // التحقق من اسم الصفحة المفتوحة لتقديم عنوان مناسب
        if (window.location.pathname.includes('gallery.html')) {
            siteTitle.innerText = translations[lang]["gallery-full-title"] + " | " + translations[lang]["logo-text"];
        } else {
            siteTitle.innerText = translations[lang]["title"];
        }
    }
    
    const logoText = document.getElementById('logo-text');
    if (logoText) logoText.innerText = translations[lang]["logo-text"];

    // 3. تحديث كافة النصوص التي تحتوي على Attribute data-key
    const elements = document.querySelectorAll('[data-key]');
    elements.forEach(element => {
        const key = element.getAttribute('data-key');
        if (translations[lang][key]) {
            element.innerHTML = translations[lang][key];
        }
    });

    // 4. تحديث الأزرار الفعالة الخاصة باختيار اللغة لتغيير تصميمها (active)
    const buttons = document.querySelectorAll('.lang-btn');
    buttons.forEach(btn => {
        btn.classList.remove('active');
        // التحقق من استدعاء اللغة لتحديد الزر الفعال
        const onclickAttr = btn.getAttribute('onclick');
        if (onclickAttr && onclickAttr.includes(lang)) {
            btn.classList.add('active');
        }
    });

    // 5. حفظ اللغة المفضلة لدى المستخدم في المتصفح لاسترجاعها عند تحديث الصفحة
    localStorage.setItem('preferredLang', lang);
}

// تشغيل اللغة عند تحميل الصفحة (تلقائياً حسب المتصفح أو الاختيار السابق)
window.addEventListener('DOMContentLoaded', () => {
    // 1. التحقق أولاً إذا كان الزائر قد اختار لغة بنفسه سابقاً داخل الموقع
    let targetLang = localStorage.getItem('preferredLang');

    // 2. إذا كانت هذه زيارته الأولى، نقرأ لغة متصفحه ونطابقها مع لغات الموقع
    if (!targetLang) {
        const browserLang = navigator.language || navigator.userLanguage; // مثل "ar-EG" أو "tr-TR" أو "en-US"
        const shortLang = browserLang.slice(0, 2).toLowerCase(); // نأخذ أول حرفين فقط لتبسيطها مثل "ar" أو "tr" أو "en"

        // إذا كانت لغة المتصفح تدعم العربية، التركية أو الإنجليزية، نعتمدها
        if (['ar', 'tr', 'en'].includes(shortLang)) {
            targetLang = shortLang;
        } else {
            targetLang = 'ar'; // لغة افتراضية احتياطية (العربية) إذا كانت لغة متصفحه غير اللغات الثلاث
        }
    }

    // 3. تطبيق اللغة وتغيير الواجهة
    changeLanguage(targetLang);
});

// كود القائمة الجانبية (الأكورديون للهواتف)
const menuToggle = document.getElementById('menuToggle');
const menu = document.getElementById('menu');

if (menuToggle && menu) {
    menuToggle.addEventListener('click', () => {
      const isOpen = menu.classList.toggle('open');
      menuToggle.setAttribute('aria-expanded', isOpen ? 'true' : 'false');
    });

    menu.querySelectorAll('a').forEach(link => {
      link.addEventListener('click', () => {
        menu.classList.remove('open');
        menuToggle.setAttribute('aria-expanded', 'false');
      });
    });
}